package controlstatements;

public class LearnOperators {

	public static void main(String[] args) {
		
		int i=3;
		int j=6;
		
		System.out.println(i+j);
		System.out.println(i-j);
        System.out.println(i/j);  // print the quotient
        System.out.println(j%j);   // print the remainder
        
        System.out.println(++i);
        System.out.println(i++);
        System.out.println(i);            //before increment i++ after increment 
        System.out.println(--j);
        System.out.println(j);
        System.out.println(j--);
        
	}
	

}
